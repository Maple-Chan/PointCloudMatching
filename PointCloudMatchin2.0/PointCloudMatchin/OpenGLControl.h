#pragma once
#include <gl/GL.h>
#include <gl/GLU.h>


class COpenGL
{
public:
	COpenGL(void);
	~COpenGL(void);
	HDC hDC;
	HGLRC hRC;

public:
	/************************************************************************/
	/*    对OpenGL的一些初始化工作，width和height表示窗口的宽和高                                                                  */
	/************************************************************************/
	void Init(int width, int height);

	/************************************************************************/
	/* 设置像素格式                                                                     */
	/************************************************************************/
	bool SetupPixelFormat(HDC hDC0);

	/************************************************************************/
	/* 窗口大小改变时的回调函数                                                                     */
	/************************************************************************/
	void Reshap(int width, int height);

	/************************************************************************/
	/*  具体的渲染操作，窗口中显示的内容是在这个函数中完成的                                                                    */
	/************************************************************************/
	void Render();
};
